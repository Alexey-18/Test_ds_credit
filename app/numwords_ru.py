"""Преобразование русских числительных, записанных словами, в число.
Модуль умеет разбирать конструкции вида «один миллион двести пятьдесят тысяч»,
которые часто встречаются в договорах и счетах рядом со словом «рублей».
Полная грамматика русских числительных (падежи, склонения) не покрывается -
этого не требуется для целей задачи (суммы прописью в финансовых документах
почти всегда пишутся в именительном/родительном падеже одним и тем же
ограниченным набором форм).
"""

from __future__ import annotations
import re
from typing import Iterable, Optional

_UNITS = {
    "ноль": 0,
    "один": 1, "одна": 1, "одно": 1,
    "два": 2, "две": 2,
    "три": 3, "четыре": 4, "пять": 5, "шесть": 6, "семь": 7,
    "восемь": 8, "девять": 9,
    "десять": 10, "одиннадцать": 11, "двенадцать": 12, "тринадцать": 13,
    "четырнадцать": 14, "пятнадцать": 15, "шестнадцать": 16,
    "семнадцать": 17, "восемнадцать": 18, "девятнадцать": 19,
    "двадцать": 20, "тридцать": 30, "сорок": 40, "пятьдесят": 50,
    "шестьдесят": 60, "семьдесят": 70, "восемьдесят": 80, "девяносто": 90,
    "сто": 100, "двести": 200, "триста": 300, "четыреста": 400,
    "пятьсот": 500, "шестьсот": 600, "семьсот": 700, "восемьсот": 800,
    "девятьсот": 900,
}
# Множители-разряды (тысяча, миллион, миллиард) во всех встречающихся формах.
_SCALES = {
    "тысяча": 1_000, "тысячи": 1_000, "тысяч": 1_000,
    "миллион": 1_000_000, "миллиона": 1_000_000, "миллионов": 1_000_000,
    "миллиард": 1_000_000_000, "миллиарда": 1_000_000_000,
    "миллиардов": 1_000_000_000,
}
_ALL_NUMBER_WORDS = set(_UNITS) | set(_SCALES)

# Регулярное выражение, которое находит цепочку числительных, стоящую сразуперед словом «рубл...» (рубль/рубля/рублей). Порядок слов в alternation неважен - re сам подбирает вхождения по всей строке.
_WORDS_PATTERN = "|".join(sorted(_ALL_NUMBER_WORDS, key=len, reverse=True))
AMOUNT_IN_WORDS_RE = re.compile(
    rf"((?:(?:{_WORDS_PATTERN})\s+){{1,10}})руб",
    re.IGNORECASE,
)

def words_to_number(tokens: Iterable[str]) -> Optional[int]:
    """Преобразует последовательность слов-числительных в целое число.
    Возвращает ``None``, если ни одно слово не удалось распознать.
    """
    total = 0
    current = 0
    found = False
    for raw in tokens:
        tok = raw.strip(".,").lower()
        if tok in _UNITS:
            current += _UNITS[tok]
            found = True
        elif tok in _SCALES:
            scale = _SCALES[tok]
            current = current or 1
            total += current * scale
            current = 0
            found = True
        # неизвестные слова внутри уже найденной цепочки просто игнорируются(например, случайные предлоги); за пределами цепочки функция невызывается, т.к. вызывающий код передаёт уже вычлененный фрагмент.
    total += current
    return total if found else None

def find_amount_in_words(text: str) -> Optional[float]:
    """Ищет в тексте сумму, записанную словами перед «рубл...», и возвращает её.
    Пример: "девятьсот тысяч рублей 00/100" -> 900000.0
    """
    match = AMOUNT_IN_WORDS_RE.search(text)
    if not match:
        return None
    tokens = match.group(1).split()
    value = words_to_number(tokens)
    return float(value) if value is not None else None
